function generateProductRows(products, tbody) {

    tbody.innerHTML = '';

    products.forEach(product => {
        const row = document.createElement('tr');
        row.innerHTML = `
            <td>${product.name}</td>
            <td>${product.price}</td>
            <td><a href="${product.link}" class="custom-button" target="_blank">Boek nu!</a></td>
        `;
        tbody.appendChild(row);
    });
}

// Call the functions to generate product rows for each section when the page loads
window.onload = function() {
    generateProductRowsSection1();
    generateProductRowsSection2();
    generateProductRowsSection3();
    generateProductRowsSection4();
};
